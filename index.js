app.post("/login", async (req, res) => {
  const { email, password } = req.body;
  console.log(req.body);

  try {
    // Check in Merchant schema
    const merchantUser = await Merchant.findOne({
      email: email,
      status: "Allow",
    });
    if (merchantUser) {
      bcrypt.compare(password, merchantUser.password, (err, result) => {
        if (result) {
          const token = jwt.sign(
            { userId: merchantUser._id },
            process.env.TOKEN_KEY
          );
          return res.status(200).json({
            token: `${token}`,
            userType: `${merchantUser.user_type}`,
            username: `${merchantUser.user_name}`,
          });
        } else {
          return res.status(401).json({ message: "Wrong email or password" });
        }
      });
    } else {
      // Check in SinghTekUser schema
      const user = await SinghtekUser.findOne({ email: email });
      if (user) {
        bcrypt.compare(password, user.password, (err, result) => {
          if (result) {
            const token = jwt.sign({ userId: user._id }, process.env.TOKEN_KEY);
            return res.status(200).json({
              token: `${token}`,
              userType: `${user.user_type}`,
              username: `${user.username}`,
            });
          } else {
            return res.status(401).json({ message: "Wrong email or password" });
          }
        });
      } else {
        // Check in Admin schema
        const adminUser = await Admin.findOne({ email: email });
        if (adminUser) {
          bcrypt.compare(password, adminUser.password, (err, result) => {
            if (result) {
              const token = jwt.sign(
                { userId: adminUser._id },
                process.env.TOKEN_KEY
              );
              return res
                .status(200)
                .json({ token: `${token}`, Username: `${adminUser.username}` });
            } else {
              return res
                .status(401)
                .json({ message: "Wrong email or password" });
            }
          });
        } else {
          return res.status(401).json({ message: "User not found" });
        }
      }
    }
  } catch (error) {
    return res.status(401).json({ error: "Error while logging in" });
  }
});

